package kodlamaio.hrms.business.validationRules;

import kodlamaio.hrms.business.constants.Messages;
import kodlamaio.hrms.core.utilities.result.ErrorResult;
import kodlamaio.hrms.core.utilities.result.Result;
import kodlamaio.hrms.core.utilities.result.SuccessResult;
import kodlamaio.hrms.dataAccess.abstracts.UserDao;
import org.springframework.beans.factory.annotation.Autowired;

import java.util.regex.Matcher;
import java.util.regex.Pattern;

public class UserValidator implements BaseValidator {

    @Autowired
    public UserValidator(UserDao userDao) {
        this.userDao = userDao;
    }

    private UserDao userDao;

    public Result isEmailValid(String email) {
        Pattern pattern = Pattern.compile("(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|\"(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21\\x23-\\x5b\\x5d-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])*\")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21-\\x5a\\x53-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])+)\\])");
        Matcher m = pattern.matcher(email);
        if (!m.matches())
            return new ErrorResult(Messages.EmailIsInvalid);

        return new SuccessResult();
    }

    public Result arePasswordsSame(String password, String passwordRepeat) {
        if (!password.equals(passwordRepeat))
            return new ErrorResult(Messages.PasswordsNotSame);
        return new SuccessResult();
    }

    public Result isEmailTaken(String email) {
        if (userDao.existsUserByEmail(email))
            return new ErrorResult(Messages.EmailAlreadyTaken);
        return new SuccessResult();
    }

}
